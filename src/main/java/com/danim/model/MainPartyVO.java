package com.danim.model;

public class MainPartyVO {
	private int do_id;
	private int view_count;
	private int party_num;
	private String party_thumbnail;
	
	public int getDo_id() {
		return do_id;
	}
	public void setDo_id(int do_id) {
		this.do_id = do_id;
	}
	public int getView_count() {
		return view_count;
	}
	public void setView_count(int view_count) {
		this.view_count = view_count;
	}
	public int getParty_num() {
		return party_num;
	}
	public void setParty_num(int party_num) {
		this.party_num = party_num;
	}
	public String getParty_thumbnail() {
		return party_thumbnail;
	}
	public void setParty_thumbnail(String party_thumbnail) {
		this.party_thumbnail = party_thumbnail;
	}
	
	

}
